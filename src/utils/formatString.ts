export const mono = (str: string): string => `\`${str}\``;
export const bold = (str: string): string => `*${str}*`;
export const italic = (str: string): string => `_${str}_`;
export const code = (str: string): string => `\`\`\`${str}\`\`\``;
export const brackets = (str: string): string => `\\[ ${str} ]`;
